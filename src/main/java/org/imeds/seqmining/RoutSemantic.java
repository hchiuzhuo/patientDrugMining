package org.imeds.seqmining;

public class RoutSemantic {
		private Integer attrNum;
		private Integer RoutId;
		private Integer semanticSeqId;
		private String RoutName;
		private String semanticSeqName;
		
		
		public Integer getAttrNum() {
			return attrNum;
		}
		public void setAttrNum(Integer attrNum) {
			this.attrNum = attrNum;
		}
		public Integer getRoutId() {
			return RoutId;
		}
		public void setRoutId(Integer routId) {
			RoutId = routId;
		}
		public Integer getSemanticSeqId() {
			return semanticSeqId;
		}
		public void setSemanticSeqId(Integer semanticSeqId) {
			this.semanticSeqId = semanticSeqId;
		}
		public String getRoutName() {
			return RoutName;
		}
		public void setRoutName(String routName) {
			RoutName = routName;
		}
		public String getSemanticSeqName() {
			return semanticSeqName;
		}
		public void setSemanticSeqName(String semanticSeqName) {
			this.semanticSeqName = semanticSeqName;
		}
	
}
